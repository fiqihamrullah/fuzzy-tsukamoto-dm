     public void loadDataVariable()
    {
        DataProcessor dp = new DataProcessor();
        listvariable = dp.loadListVariable();
        String header[] = new String[]{"No","Nama Variabel","Nilai"};
        Object data[][] = new Object[listvariable.size()][header.length];
        datpasien = new DataPasien();
        for(int i=0;i<listvariable.size();i++)
        {
              datpasien.AddAtribute(listvariable.get(i));
             data[i][0] = i+1;
             data[i][1] = listvariable.get(i).getNmVar();
             data[i][2] = "0";
                     
        }
       jTblViewDiagnosa.setModel(new DefaultTableModel(data, header));
    }
