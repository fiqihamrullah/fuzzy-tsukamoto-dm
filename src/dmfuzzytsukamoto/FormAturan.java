private void jBtnTambahDetailAturanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTambahDetailAturanActionPerformed
// TODO add your handling code here:
    Aturan aturan = new Aturan();
    aturan.setNoAturan(listaturan.get(idxrow).getNoAturan());
    Himpunan himp = new Himpunan();
    himp.getVar().setIdVar(listvariable.get(jCmbVariable.getSelectedIndex()).getIdVar());
    himp.setIdHimp(listhimpunan.get(jCmbHimpunan.getSelectedIndex()).getIdHimp());
    DataProcessor dp = new DataProcessor();
    dp.SimpanAntecedent(aturan, himp);
    loadDetailAturan(listaturan.get(idxrow).getNoAturan());
    JOptionPane.showMessageDialog(this, "Data Detail Aturan(Anteseden) Berhasil Disimpan!","Sukses",JOptionPane.INFORMATION_MESSAGE);
}//GEN-LAST:event_jBtnTambahDetailAturanActionPerformed

private void jBtnHapusDetailAturanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnHapusDetailAturanActionPerformed
// TODO add your handling code here:
    int respon = JOptionPane.showConfirmDialog(this,"Apakah Anda yakin akan menghapus Detail Aturan tersebut?","Konfirmasi",JOptionPane.YES_NO_OPTION);
    if (respon == JOptionPane.YES_NO_OPTION)
    {
        DataProcessor dp = new DataProcessor();
        dp.HapusAntecedent(listdetailaturan.get(idxrow2).getVar().getIdVar(),listdetailaturan.get(idxrow2).getIdHimp(),listaturan.get(idxrow).getNoAturan());
        loadDetailAturan(listaturan.get(idxrow).getNoAturan());
        JOptionPane.showMessageDialog(this, "Data Detail Aturan Berhasil Dihapus!","Sukses",JOptionPane.INFORMATION_MESSAGE);
    }
}//GEN-LAST:event_jBtnHapusDetailAturanActionPerformed

