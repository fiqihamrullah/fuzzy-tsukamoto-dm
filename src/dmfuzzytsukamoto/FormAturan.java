private void jBtnTambahAturanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTambahAturanActionPerformed
// TODO add your handling code here:
    Aturan aturan = new Aturan();
    aturan.setNoAturan(jTxtNoAturan.getText());
    aturan.setKep(jCmbResikoDM.getSelectedIndex());
    DataProcessor dp = new DataProcessor();
    dp.SimpanAturan(aturan);
    loadDataAturan();
    JOptionPane.showMessageDialog(this, "Data Aturan Berhasil Disimpan!","Sukses",JOptionPane.INFORMATION_MESSAGE);
}//GEN-LAST:event_jBtnTambahAturanActionPerformed

private void jBtnHapusAturanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnHapusAturanActionPerformed
// TODO add your handling code here:
    int respon = JOptionPane.showConfirmDialog(this,"Apakah Anda yakin akan menghapus aturan tersebut?","Konfirmasi",JOptionPane.YES_NO_OPTION);
    if (respon == JOptionPane.YES_NO_OPTION)
    {
        DataProcessor dp = new DataProcessor();
        dp.HapusAturan(listaturan.get(idxrow));
        loadDataAturan();
        JOptionPane.showMessageDialog(this, "Data Aturan Berhasil Dihapus!","Sukses",JOptionPane.INFORMATION_MESSAGE);
    }
}//GEN-LAST:event_jBtnHapusAturanActionPerformed

