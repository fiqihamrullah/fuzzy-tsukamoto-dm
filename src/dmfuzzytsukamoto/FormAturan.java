     public void fillDataComboVariable()
    {
        DataProcessor dp = new DataProcessor();
        listvariable = dp.loadListVariable();
        for(int i=0;i<listvariable.size();i++) 
        {
            jCmbVariable.addItem(listvariable.get(i).getNmVar());
        }
    }
     
       public void fillDataComboHimpunan(int idvar)
    {
        DataProcessor dp = new DataProcessor();
        jCmbHimpunan.removeAllItems();
        listhimpunan = dp.loadListHimpunan(idvar);
        for(int i=0;i<listhimpunan.size();i++) 
        {
            jCmbHimpunan.addItem(listhimpunan.get(i).getNmhimp());
        }
    }

    public void loadDataAturan()
    {
        DataProcessor dp = new DataProcessor();
        listaturan = dp.loadListAturan();
        String header[] = new String[] {"No","No Aturan","Keputusan(Consequent)"};
        jTblAturan.setModel(TableViewer.ConvertAturanListToTableModel(listaturan, header));
    }
    
    public void loadDetailAturan(String noaturan)
    {
        DataProcessor dp = new DataProcessor();
        listdetailaturan = dp.loadDetailListAturan(noaturan);
        String header[] = new String[] {"No","Nama Variabel","Himpunan"};
        jTblDetailAturan.setModel(TableViewer.ConvertDetailAturanListToTableModel(listdetailaturan,header));
    }
    
